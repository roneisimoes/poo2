public class capitulo {
        private int numero;
        private livro livro;

    public capitulo(int numero, livro livro) {
        this.numero = numero;
        this.livro = livro;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public livro getLivro() {
        return livro;
    }

    public void setLivro(livro livro) {
        this.livro = livro;
    }
    
        
        
}
